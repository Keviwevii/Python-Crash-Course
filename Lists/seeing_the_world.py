#List of locations I want to visit

locations = ['Dubai', 'Las Vegas', 'Colorado', 'Mexico City', 'Mykonos']

#Original order

print(locations)

#Temporaily sorted

print(sorted(locations))

#It's still in order

print(locations)

#List in reverse

locations.reverse()
print(locations)

#Reverse again

locations.reverse()
print(locations)

#Sort list

locations.sort()
print(locations)

#Sorted backwards

locations.sort(reverse=True)
print(locations)